class TestPushBackupBehavior:

    def test_backup_not_overwritten_on_second_push(self, jira, art_dir):
        jira.create("RHAISTRAT-1020", "Backup idempotency",
                     "## Business Need\n\nOriginal content.")

        local_file = art_dir / "artifacts" / "strat-tasks" / "RHAISTRAT-1020.md"

        local_file.write_text(
            f"{STRATEGY_HEADING}\n\nFirst push.\n"
        )
        result1 = _run(jira, "RHAISTRAT-1020", local_file)
        assert result1.returncode == 0, f"stderr: {result1.stderr}"

        backup_path = art_dir / "artifacts" / "strat-originals" / "RHAISTRAT-1020-pre-push.md"
        assert backup_path.exists()
        first_backup = backup_path.read_text()
        assert "Original content" in first_backup

        local_file.write_text(
            f"{STRATEGY_HEADING}\n\nSecond push.\n"
        )
        result2 = _run(jira, "RHAISTRAT-1020", local_file)
        assert result2.returncode == 0, f"stderr: {result2.stderr}"

        second_backup = backup_path.read_text()
        assert second_backup == first_backup

    def test_no_backup_when_description_empty(self, jira, art_dir):
        jira.create("RHAISTRAT-1021", "Empty desc", "")

        local_file = art_dir / "artifacts" / "strat-tasks" / "RHAISTRAT-1021.md"
        local_file.write_text(
            f"{STRATEGY_HEADING}\n\nContent.\n"
        )

        result = _run(jira, "RHAISTRAT-1021", local_file)
        assert result.returncode == 0, f"stderr: {result.stderr}"

        backup_path = art_dir / "artifacts" / "strat-originals" / "RHAISTRAT-1021-pre-push.md"
        assert not backup_path.exists()


