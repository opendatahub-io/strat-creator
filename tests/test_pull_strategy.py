    def test_round_trip_push_pull(self, jira, tmp_path):
        """Push a large strategy (goes to attachment), pull it back,
        verify the local file has the full strategy content."""
        jira.create("RHAISTRAT-2042", "Round trip",
                     "## Business Need\n\nContext for round-trip test.",
                     labels=["strat-creator-rubric-pass"])
        jira.create("RHAIRFE-3042", "RFE for round trip", "Some RFE.")
        jira.request("POST", "/rest/api/3/issueLink", {
            "type": {"name": "Cloners"},
            "inwardIssue": {"key": "RHAIRFE-3042"},
            "outwardIssue": {"key": "RHAISTRAT-2042"},
        })

        push_script = os.path.join(PROJECT_ROOT, "scripts", "push_strategy.py")
        push_dir = tmp_path / "push-workspace" / "artifacts"
        (push_dir / "strat-tasks").mkdir(parents=True)
        (push_dir / "strat-originals").mkdir(parents=True)

        lines = [f"{STRATEGY_HEADING}\n", "### TL;DR\n",
                 "Round-trip test summary.\n", "### Technical Approach\n"]
        while sum(len(l) for l in lines) < 25000:
            lines.append(
                "Detailed paragraph about GPU time-slicing implementation "
                "approach using NVIDIA MIG profiles and device plugins.\n\n")
        large_strategy = "\n".join(lines)

        push_file = push_dir / "strat-tasks" / "RHAISTRAT-2042.md"
        push_file.write_text(large_strategy)

        push_env = {
            **os.environ,
            "JIRA_SERVER": jira.url,
            "JIRA_USER": "admin",
            "JIRA_TOKEN": "admin",
        }
        push_result = subprocess.run(
            [sys.executable, push_script, "RHAISTRAT-2042", str(push_file)],
            env=push_env, capture_output=True, text=True, cwd=PROJECT_ROOT)
        assert push_result.returncode == 0, f"push stderr: {push_result.stderr}"

        local_dir = tmp_path / "local"
        pull_result = _run(jira, "RHAISTRAT-2042", local_dir)
        assert pull_result.returncode == 0, f"pull stderr: {pull_result.stderr}"

        pulled_path = local_dir / "strat-tasks" / "RHAISTRAT-2042.md"
        pulled_content = pulled_path.read_text()
        assert "### Technical Approach" in pulled_content
        assert "Round-trip test summary" in pulled_content
        assert "GPU time-slicing" in pulled_content


