def _upload_strategy_attachment(jira, strat_key, strategy_content):
    """Upload a strategy markdown file as an attachment."""
    with tempfile.NamedTemporaryFile(
            mode="w", suffix=".md", delete=False, encoding="utf-8") as f:
        f.write(strategy_content)
        tmp_path = f.name
    try:
        add_attachment(jira.url, "admin", "admin", strat_key,
                       tmp_path, filename=f"{strat_key}-strategy.md")
    finally:
        os.unlink(tmp_path)


