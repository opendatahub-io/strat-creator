class TestBuildDescriptionStub:

    def test_stub_with_tldr(self):
        existing_md = (
            "## Business Need\n\n"
            "Users need GPU sharing.\n\n"
            f"{STRATEGY_HEADING}\n\n"
            "### TL;DR\n\n"
            "Enable GPU sharing.\n\n"
            "### Technical Approach\n\n"
            "Use MIG.\n"
        )
        strategy_section = (
            f"{STRATEGY_HEADING}\n\n"
            "### TL;DR\n\n"
            "Enable GPU sharing.\n\n"
            "### Technical Approach\n\n"
            "Use MIG.\n"
        )
        result = _build_description_stub(
            existing_md, strategy_section, None, "RHAISTRAT-1000-strategy.md")
        assert "Business Need" in result
        assert "Users need GPU sharing" in result
        assert STRATEGY_HEADING in result
        assert "### TL;DR" in result
        assert "Enable GPU sharing" in result
        assert "### Technical Approach" not in result
        assert "RHAISTRAT-1000-strategy.md" in result

    def test_stub_without_tldr(self):
        existing_md = "## Business Need\n\nContext.\n"
        strategy_section = (
            f"{STRATEGY_HEADING}\n\n"
            "### Technical Approach\n\n"
            "Details.\n"
        )
        result = _build_description_stub(
            existing_md, strategy_section, None, "RHAISTRAT-1000-strategy.md")
        assert STRATEGY_HEADING in result
        assert "### TL;DR" not in result
        assert "RHAISTRAT-1000-strategy.md" in result

    def test_stub_preserves_business_need(self):
        existing_md = (
            "## Business Need\n\n"
            "Critical business context that must be preserved.\n\n"
            f"{STRATEGY_HEADING}\n\n"
            "Old strategy.\n"
        )
        strategy_section = f"{STRATEGY_HEADING}\n\nNew strategy.\n"
        result = _build_description_stub(
            existing_md, strategy_section, None, "RHAISTRAT-1000-strategy.md")
        assert "Critical business context that must be preserved" in result

    def test_stub_includes_staff_input_attachment_notice(self):
        existing_md = "## Business Need\n\nContext.\n"
        strategy_section = f"{STRATEGY_HEADING}\n\nStrategy.\n"
        result = _build_description_stub(
            existing_md, strategy_section, "RHAISTRAT-1000-strategy.md",
            staff_input_attachment_filename="RHAISTRAT-1000-staff-input.md")
        assert STAFF_INPUT_HEADING in result
        assert "RHAISTRAT-1000-staff-input.md" in result
        assert "stored as attachment" in result

    def test_stub_adds_staff_input_template_when_missing(self):
        existing_md = "## Business Need\n\nContext.\n"
        strategy_section = f"{STRATEGY_HEADING}\n\nStrategy.\n"
        result = _build_description_stub(
            existing_md, strategy_section, None, "RHAISTRAT-1000-strategy.md")
        assert STAFF_INPUT_HEADING in result

    def test_stub_does_not_duplicate_staff_input(self):
        existing_md = (
            "## Business Need\n\nContext.\n\n"
            f"{STAFF_INPUT_HEADING}\n\nExisting notes.\n"
        )
        strategy_section = f"{STRATEGY_HEADING}\n\nStrategy.\n"
        result = _build_description_stub(
            existing_md, strategy_section, "RHAISTRAT-1000-strategy.md",
            staff_input_attachment_filename="RHAISTRAT-1000-staff-input.md")
        count = result.count(STAFF_INPUT_HEADING)
        assert count == 1
        assert "Existing notes" not in result
        assert "RHAISTRAT-1000-staff-input.md" in result


