        assert result is not None
        assert "Enable time-sliced GPU sharing via MIG." in result
        assert "Use the NVIDIA device plugin." in result
        assert "Business Need" not in result
        assert "Users need GPU sharing" not in result
        assert "Staff Engineer / SME Input" not in result

