class TestExtractStrategySection:

    def test_extracts_strategy_section(self):
        content = (
            "## Strategy (AI Generated by Agentic SDLC Pipeline)\n\n"
            "This is the strategy content.\n\n"
            "### Sub-section\n\n"
            "More details here.\n"
        )
        result = extract_strategy_section(content)
        assert result is not None
        assert "## Strategy (AI Generated by Agentic SDLC Pipeline)" in result
        assert "This is the strategy content." in result
        assert "More details here." in result

    def test_returns_none_when_no_strategy_section(self):
        content = (
            "## Business Need\n\n"
            "We need this feature.\n\n"
            "## Scope\n\n"
            "It is small.\n"
        )
        result = extract_strategy_section(content)
